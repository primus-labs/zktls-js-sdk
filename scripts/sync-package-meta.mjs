/**
 * Writes src/generated/packageMeta.ts from root package.json so runtime/bundler
 * output needs no `import ... assert { type: 'json' }` (breaks older Node).
 */
import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const pkg = JSON.parse(readFileSync(join(root, 'package.json'), 'utf8'));

const outDir = join(root, 'src', 'generated');
mkdirSync(outDir, { recursive: true });

const esc = (s) => String(s).replace(/\\/g, '\\\\').replace(/'/g, "\\'");

const contents = `/* Generated by scripts/sync-package-meta.mjs from package.json — do not edit. */
export const PACKAGE_VERSION = '${esc(pkg.version)}';
export const PACKAGE_NAME = '${esc(pkg.name)}';
`;

writeFileSync(join(outDir, 'packageMeta.ts'), contents);
